namespace CSharpEnhanced.Maths
{
	/// <summary>
	/// Enum denoting type of angle unit
	/// </summary>
	public enum AngleUnit
	{
		/// <summary>
		/// Radians - 2pi radians gives a full turn
		/// </summary>
		Radians = 0,

		/// <summary>
		/// Degrees - 360 degrees gives a full turn
		/// </summary>
		Degrees = 1,

		/// <summary>
		/// Turn - angle denoting a full circle
		/// </summary>
		Turns = 2
	}
}